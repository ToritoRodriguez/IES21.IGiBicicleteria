    private JTextField nombreCategoriaField;
    private JComboBox<String> tipoCategoriaComboBox;
    
    public AltaCategoria() {
        setTitle("Alta Categoría");
        setSize(400, 250);
        setDefaultCloseOperation(EXIT_ON_CLOSE);
        setLayout(new GridLayout(4, 2, 10, 10));

        // Agregamos los componentes del formulario
        add(new JLabel("Tipo de Categoría:"));
        tipoCategoriaComboBox = new JComboBox<>(new String[]{"BICICLETA", "ACCESORIO", "REPUESTO"});
        add(tipoCategoriaComboBox);

        add(new JLabel("Nombre de la Categoría:"));
        nombreCategoriaField = new JTextField();
        add(nombreCategoriaField);

        JButton submitButton = new JButton("Dar de alta");
        submitButton.addActionListener(new SubmitButtonListener());
        add(submitButton);

        JButton backButton = new JButton("Volver");
        backButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                new HomeMenuProductos().setVisible(true);
                dispose();
            }
        });
        add(backButton);
    }

    private class SubmitButtonListener implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent e) {
            try {
                // Obtener los datos del formulario
                String nombreCategoria = nombreCategoriaField.getText();
                String tipoCategoria = (String) tipoCategoriaComboBox.getSelectedItem();  

                if (nombreCategoria.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "El nombre de la categoría no puede estar vacío.", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                // Convertir tipoCategoria (String) a tipo enumerado
                TipoCategoria tipo = null;
                if (tipoCategoria.equals("BICICLETA")) {
                    tipo = TipoCategoria.BICICLETA;
                } else if (tipoCategoria.equals("ACCESORIO")) {
                    tipo = TipoCategoria.ACCESORIO;
                } else if (tipoCategoria.equals("REPUESTO")) {
                    tipo = TipoCategoria.RESPUESTO;
                }

                // Crear un objeto Categoria
                Categoria categoria = new Categoria(nombreCategoria, tipo);

                // Crear una instancia de ABMCategoria y dar de alta la categoría
                ABMCategoria abmCategoria = new ABMCategoria();
                abmCategoria.altaCategoria(categoria, tipo.ordinal());  // Aquí se pasa el tipo como un número entero

                // Mostrar un mensaje de éxito
                JOptionPane.showMessageDialog(null, "Categoría dada de alta exitosamente.");
            } catch (Exception ex) {
                // Manejo de excepciones
                JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

